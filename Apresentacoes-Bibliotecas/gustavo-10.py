'''O TensorFlow é uma biblioteca de software de código aberto para computação numérica e inteligência artificial (IA): 
Foi criada pelo Google e lançada em novembro de 2015.

É usada em projetos de IA, como machine learning e deep learning.
Permite que desenvolvedores implementem modelos de machine learning de forma mais rápida e fácil 
Tem uma estrutura muito semelhante ao Python 
Possui algoritmos prontos para serem utilizados em todas as três grandes fases do desenvolvimento de algoritmos de aprendizagem de máquina 
Utiliza o poder de processamento paralelo de GPUs (graphic processing units)
 
Algumas das principais funcionalidades do TensorFlow são:
Criação e treinamento de modelos de redes neurais
Processamento de dados em grande escala
Deploy de modelos em produção
Experimentação rápida com diferentes arquiteturas de redes neurais '''
